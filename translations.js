function siteTranslate(lang) {
    function walk(node) {
        if (node.nodeType === Node.TEXT_NODE) {
            let text = node.nodeValue.trim();
            if (text) {
                // Save original text once on the node object
                if (!node.customI18nOriginal) {
                    node.customI18nOriginal = text;
                }
                
                const ptKey = node.customI18nOriginal;
                if (dict[ptKey]) {
                    if (lang === 'pt') {
                        node.nodeValue = node.nodeValue.replace(node.nodeValue.trim(), ptKey);
                    } else if (dict[ptKey][lang]) {
                        node.nodeValue = node.nodeValue.replace(node.nodeValue.trim(), dict[ptKey][lang]);
                    }
                }
            }
        } else if (node.nodeType === Node.ELEMENT_NODE) {
            // Ignore scripts, styles, inputs...
            if (['SCRIPT', 'STYLE', 'NOSCRIPT'].includes(node.tagName) || node.classList.contains('lang-menu')) return;
            node.childNodes.forEach(child => walk(child));
        }
    }
    walk(document.body);
    document.documentElement.lang = lang;
}

window.applyStoredTranslation = function() {
    const lang = localStorage.getItem('siteLang') || 'pt';
    siteTranslate(lang);
};

document.addEventListener('DOMContentLoaded', () => {
    window.applyStoredTranslation();
});
